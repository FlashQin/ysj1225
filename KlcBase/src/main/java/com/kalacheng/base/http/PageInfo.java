package com.kalacheng.base.http;

public class PageInfo {


    /*
     * 每页查询多少条
     */
    public int pageSize;

    /*
     * 查询第几页
     */
    public int pageIndex;

    /*
     * 返回数据，总共有多少页
     */
    public int outTotalPage;

    /*
     * 返回数据，总共有多少条记录
     */
    public int outTotalCount;

}
